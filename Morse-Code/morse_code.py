class MorseCodeDecisionTree:

    def __init__(self):
        self.root = _TreeNode(None)

    @staticmethod
    def create_decision_tree(codes: dict):
        code = '-'
        node = MorseCodeDecisionTree()
        while len(code) != 5:
            code += '.'
            # building left subtree
            if code in codes.keys():
                node.left = codes[code]
            code




class _TreeNode:
    def __init__(self, letter):
        self.data = letter
        self.left = None
        self.right = None



